#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	if (!s)
		return ;
	write(fd, s, ft_strlen(s));
}
/*
putstr_fd suit la meme logique qu'un putstr classique
Mais imprimme dans le fd passe en param
*/
