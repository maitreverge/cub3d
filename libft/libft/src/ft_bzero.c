#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	ft_memset(s, 0, n);
}
/*
ft_bzero initialise un void* avec des 0 dans tout le buffer,
afin d'eviter des valeurs "parasites" de se glisser

*/
