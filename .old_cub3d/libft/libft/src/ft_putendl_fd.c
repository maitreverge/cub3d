#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	ft_putstr_fd(s, fd);
	ft_putchar_fd('\n', fd);
}
/*
putendl_fd se contente d'imprimer la chaine de caractere sur le fd
donne en paramettre
Et termine par un retour a la ligne
*/
